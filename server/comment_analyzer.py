class SentimentAnalyzer:
    def __init__(self):
        self.positive_words = set([
            '好听', '喜欢', '爱', '棒', '赞', '感动', '经典', '回忆', '青春',
            '好听哭', '神曲', '循环', '洗脑', '治愈', '温暖', '怀念', '完美',
            '永远', '支持', '加油', '优秀', '神级', '无敌', '炸裂', '神仙',
            '太好听', '超级好听', '非常好听', '最好听', '好听爆了',
            '永恒', '回忆杀', '青春回忆', '百听不厌', '单曲循环',
            '泪目', '哭了', '走心', '戳心', '触动', '共鸣',
            '厉害', '牛逼', '太强', '绝了', '绝绝子', 'YYDS', '顶流',
            '爱了爱了', '大爱', '超爱', '深爱', '痴迷',
            '无瑕', '无可挑剔', '惊艳', '惊喜', '震撼',
            '力挺', '守护', '陪伴', '感谢', '感恩'
        ])
        self.negative_words = set([
            '难听', '垃圾', '难听死', '恶心', '糟糕', '失望', '难听哭',
            '退钱', '翻车', '无语', '尴尬', '毁了', '抄袭', '难听爆了',
            '太难听', '超级难听', '非常难听', '最难听',
            '辣鸡', '糟', '差', '烂', '想吐',
            '绝望', '心碎', '难受', '痛苦', '悲伤',
            '无奈', '丢人', '可笑', '滑稽',
            '糟蹋', '浪费', '可惜', '遗憾', '惋惜',
            '模仿', '照搬', '雷同', '山寨', '盗版'
        ])
        
    def analyze(self, text):
        if not text or not isinstance(text, str):
            return {'sentiment': 'neutral', 'score': 0.5}
            
        text = str(text).lower()
        pos_count = sum(1 for word in self.positive_words if word in text)
        neg_count = sum(1 for word in self.negative_words if word in text)
        
        if pos_count > neg_count:
            return {'sentiment': 'positive', 'score': min(1.0, pos_count / (pos_count + neg_count + 1) * 2)}
        elif neg_count > pos_count:
            return {'sentiment': 'negative', 'score': min(1.0, neg_count / (pos_count + neg_count + 1) * 2)}
        else:
            return {'sentiment': 'neutral', 'score': 0.5}

